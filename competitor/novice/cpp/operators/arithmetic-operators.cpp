#include <bits/stdc++.h>
using namespace std;

/* Arithmetic operators
 * are used to perform arithmetic operations on variables and data.
 * Operator | Operation
 *    +    	| Addition
 *    -	    | Subtraction
 *    *	    | Multiplication
 *    /	    | Division
 *    %	    | Modulo Operation (Remainder after division)
 */

int main() {
  int a = 7, b = 2;

  printf("a + b = %d\n", a + b);
  printf("a - b = %d\n", a - b);
  printf("a * b = %d\n", a * b);
  printf("a / b = %d\n", a / b);
  printf("a %% b = %d\n", a % b);

  return 0;
}

/* output:
 *  a + b = 9
 *  a - b = 5
 *  a * b = 14
 *  a / b = 3
 *  a % b = 1
 */