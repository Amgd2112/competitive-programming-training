#include <bits/stdc++.h>
using namespace std;

int main(int argc, char const *argv[]) {
  short age = 70;
  // varaible x of size 32bit and it's equal to 5
  int x = 5;
  long long money = 289328923;                    // -2^(32) -> 2^(32)
  unsigned long long balance = 3928392832923892;  // 0 -> 2^(64)

  cout << age + 20 << endl;
  cout << x << endl;
  cout << money << endl;
  cout << balance << endl;

  return 0;
}