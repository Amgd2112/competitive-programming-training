#include <bits/stdc++.h>
using namespace std;

/* Naming
 *
 * int => x, y, n, m, q, p
 * arr => a, b, c | as name students
 * float => f, d
 * index => i, j, k
 * range = > begin, mid, end(b, m, e) | start, mid, stop
 */

int main(int argc, char const *argv[]) {
  // random value
  int n;
  cout << n << endl;

  n = 90;
  cout << n << endl;

  // with initialization
  int m = 0;

  // multi initialization: 1
  int i, j, k;
  i = j = k = 0;
  cout << i << j << k << endl;

  // multi initialization: 2
  int q, p, d = p = q = 0;
  cout << q << p << q << endl;

  return 0;
}